int		ft_find_next_prime(int nb)
{
	int i;

	i = 0;
	if (nb == 0 || nb == 1)
		return (3);
	while (i < (nb / 2))
	{
		i++;
		if (nb % i == 0 && (nb / 2) > i && i != 1)
			return (ft_find_next_prime(nb + 1));
	}
	return (nb);
}
