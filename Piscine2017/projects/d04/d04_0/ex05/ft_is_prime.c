int		ft_is_prime(int nb)
{
	int i;

	i = 0;
	if (nb == 0 || nb == 1)
		return (0);
	while (i < (nb / 2))
	{
		i++;
		if (nb % i == 0 && (nb / 2) > i && i != 1)
			return (0);
	}
	return (1);
}
