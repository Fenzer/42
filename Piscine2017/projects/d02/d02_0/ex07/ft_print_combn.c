#include <unistd.h>

/*_***************************************************************_*/

void    ft_putc(char c);
void    ft_puts(char *s);
void    ft_print_combn(int n);
int     ft_atoi(char *a);

/*_***************************************************************_*/

void    ft_puts(char *s)
{
    while (*s)
        ft_putc(*s++);
}

/*_***************************************************************_*/

void    ft_putc(char c)
{
    write(1, &c, 1);
}

/*_***************************************************************_*/

void    ft_print_combn(int n)
{
    char    t[n + 1];
    int     i;

    i = 0;
    while(i < n)
    {
        t[i] = i + '0';
        i++;
    }
    t[n] = '\0';
    
    ft_puts(t);
}

/*_***************************************************************_*/

int     ft_atoi(char *a)
{
    int r,s;

    s = 1;
    r = 0; 
    while (*a <= ' ')
        a++;
    if (*a == '-' || *a == '+')
    {
        if (*a == '-')
            s = -1;
        a++;
        if (!(*a >= '0' && *a <= '9'))
            return (r);
    }
    while (*a >= '0' && *a <= '9')
    {
        r *= 10;
        r += *a++ - '0';
    }
    return (s * r);
}
