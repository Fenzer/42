#include <unistd.h>

/*_***************************************************************_*/

void    ft_putc(char c);
void    ft_puts(char *s);
void    ft_print_combn(int n);
int     ft_atoi(char *a);
void	ft_putnbr(int nb)

/*_***************************************************************_*/

void    ft_puts(char *s)
{
    while (*s)
        ft_putc(*s++);
}

/*_***************************************************************_*/

void    ft_putc(char c)
{
    write(1, &c, 1);
}

/*_***************************************************************_*/

void    ft_print_combn(int n)
{
    int     i,n_max;

    i = 0;
    n_max = n;
    while (i++ <= n_max )
        n_max *= 10;
    i = 0;
    while (i / n_max) 
    {
        i++;
    }
}

/*_***************************************************************_*/

int     ft_atoi(char *a)
{
    int r,s;

    s = 1;
    r = 0; 
    while (*a <= ' ')
        a++;
    if (*a == '-' || *a == '+')
    {
        if (*a == '-')
            s = -1;
        a++;
        if (!(*a >= '0' && *a <= '9'))
            return (r);
    }
    while (*a >= '0' && *a <= '9')
    {
        r *= 10;
        r += *a++ - '0';
    }
    return (s * r);
}

/*_***************************************************************_*/

void	ft_putnbr(int nb)
{
    int             d;
    unsigned int    n;

    d = 1;
    if (nb < 0)
        n = -nb;;
    else
        n = nb;
    while ((n / d) > 10)
        d *= 10;
    while (n)
    {
        ft_putchar((n / d) + '0');
        n %= d;
        d /= 10;
    }
}
