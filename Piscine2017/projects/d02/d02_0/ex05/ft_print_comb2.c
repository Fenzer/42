#include <unistd.h>

void    ft_putchar(char c)
{
    write(1, &c, 1);
}

void    ft_comb_printr(int a, int b);

void    ft_print_comb2(void)
{
    int a,b;

    a = b = 0;
    while (a < 99)
    {
        b = a + 1;
        while (b <= 99)
        {
            if(a < b)
                ft_comb_printr(a,b);
            b++;
        }
        a++;
    }
}

void    ft_comb_printr(int a, int b)
{
    if(a != 98)
    {
        ft_putchar(a / 10 + '0');
        ft_putchar(a % 10 + '0');
        ft_putchar(' ');
        ft_putchar(b / 10 + '0');
        ft_putchar(b % 10 + '0');
        ft_putchar(',');
        ft_putchar(' ');
    }
    else
    {
        ft_putchar(a / 10 + '0');
        ft_putchar(a % 10 + '0');
        ft_putchar(' ');
        ft_putchar(b / 10 + '0');
        ft_putchar(b % 10 + '0');
    }
}
